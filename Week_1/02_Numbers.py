# There are 3 numeric types in Python
# Integers = int
# Floats = float
# Complex Numbers

x = 5  # int
y = 2.8  # float
z = 1j  # complex

# Basic Arithmetic

# Addition (+)
print(30 + 50)

# Subtraction (-)
print(50 - 25)

# Multiplication (*)
print(20 * 5)

# Division (/)
print(100 / 5)
print(65 / 19)

# Floor Division //
print(100 // 5)
print(65 // 19)

# Modulo % modulo operator will return the remainder
print(7 % 4)
print(11 % 3)

# Powers ** to the power
print(2 ** 3)  # 2 * 2 * 2 = 8
print(5 ** 4)  # 5 * 5 * 5 * 5 = 625

# Roots of a number
print(25 ** 0.5)
print(16 ** 0.5)

# Order of Operations
print(5 + 10 * 10 + 3)  # 108
# PEMDAS
# Parentheses,
# Exponents
# Multiplication and division left to right
# Addition and subtraction left to right

# We can use parentheses to specify orders
print((5 + 10) * (10 + 3))

# type() function returns the data type of any variable
print(type(10))
print(type(7.8))
print(type("25"))
print(type(1 + 2j))

# Variable Assignment
x = 3
print(x)

print(x + x)

x = 81
print(x)

y = x
print(y)

a = 5
print(type(a))

