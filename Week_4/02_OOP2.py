class Car:
    # Class variables
    brand = "Toyota"
    model = "Corolla"
    year = 2019
    doors = 4


# Create object from Class Car
car1 = Car()
print(car1.brand)
print(car1.model)
print(car1.year)
print(car1.doors)

# Create 2nd object from Class Car
car2 = Car()
print(car2.brand)
print(car2.model)
print(car2.year)
print(car2.doors)



