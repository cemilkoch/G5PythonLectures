# Comparison Operators compare two values and decides the relation among them
# They are also known as Relational Operators

x = 10
y = 12

# > Greater than
print("x > y", x > y)
print(type(x > y))

# < Less than
print("x < y", x < y)

# == Equal
print("x == y", x == y)

# != Not Equal
print("x != y", x != y)

# >= Greater than or equal to
print("x >= y", x >= y)

# <= Less than or equal to
print("x <= y", x <= y)

