# These operators are known as Logical operators
# and will return True if both operations are true
# or will return True if one of the operations is true

status = 5 < 10 > 8 > 6
print(status)

status_2 = 5 < 10 and 8 > 6
print(status_2)

status_3 = 7 < 15 or 7 < 5
print(status_3)

status_5 = (25 == 25 or 27 < 24) and 23 < 2
print(status_5)
