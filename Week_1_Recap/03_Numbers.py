# int
# float
# complex numbers

# whole numbers == int
x = 5

# decimal numbers == float
y = 3.14

# complex numbers == complex
z = 2j

# type() to get the type of a value
print(type(x))
print(type(y))
print(type(z))

# Data Type Conversion

# convert from int to float
b = float(x)

# convert from float to int
d = int(y)

# convert from int to complex
f = complex(x)

print(b)
print(d)
print(f)
